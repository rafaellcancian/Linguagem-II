    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        if (verificarCampos() == false) {
            Pessoa pessoa = new Pessoa();
            pessoa.setNome(jTextFieldNome.getText());
            pessoa.setEmail(jTextFieldEmail.getText());
            pessoa.setEscolaridade(jComboEscolaridade.getSelectedItem().toString());

            model.addRow(new String[]{pessoa.getNome(), pessoa.getEmail(), pessoa.getEscolaridade()});
            limparCampos();
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

