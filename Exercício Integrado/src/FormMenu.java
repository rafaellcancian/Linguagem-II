    public void limparCampos() {
        jTextFieldNome.setText("");
        jTextFieldEmail.setText("");
        jComboEscolaridade.setSelectedIndex(-1);
    }

    public boolean verificarCampos() {
        boolean erro = false;

        if ((jTextFieldNome.getText().isEmpty()) || (jTextFieldEmail.getText().isEmpty()) || (jComboEscolaridade.getSelectedIndex() == -1)) {
            JOptionPane.showMessageDialog(this, "Por favor preencha todos os campos corretamente.", "Mensagem de erro", JOptionPane.ERROR_MESSAGE);
            erro = true;
        }
        return erro;
    }

